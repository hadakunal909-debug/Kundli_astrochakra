"use client";

import { useEffect, useState } from "react";
import Link from "next/link";
import { loadResult } from "@/lib/api-client";
import type { KundliResponse } from "@/lib/types";
import { formatDate } from "@/lib/format";
import Overview from "@/components/Overview";
import Charts from "@/components/Charts";
import DivisionalCharts from "@/components/DivisionalCharts";
import PlanetTable from "@/components/PlanetTable";
import DashaTimeline from "@/components/DashaTimeline";
import Ashtakavarga from "@/components/Ashtakavarga";
import Doshas from "@/components/Doshas";
import Yogas from "@/components/Yogas";
import Numerology from "@/components/Numerology";
import Predictions from "@/components/Predictions";
import Remedies from "@/components/Remedies";
import Transit from "@/components/Transit";
import FriendshipTable from "@/components/FriendshipTable";
import KpTable from "@/components/KpTable";
import JaiminiPanel from "@/components/JaiminiPanel";
import YoginiDasha from "@/components/YoginiDasha";
import Varshaphal from "@/components/Varshaphal";
import Shadbala from "@/components/Shadbala";
import LalKitab from "@/components/LalKitab";
import PremiumReport from "@/components/premium/PremiumReport";
import PanchangPanel from "@/components/PanchangPanel";
import DownloadButtons from "@/components/DownloadButtons";

const NAV: { label: string; tabs: string[] }[] = [
  { label: "Premium", tabs: ["Premium Report"] },
  { label: "Overview", tabs: ["Chart", "Planets"] },
  { label: "Charts", tabs: ["Divisional"] },
  { label: "Dashas", tabs: ["Vimshottari", "Yogini", "Varshaphal"] },
  { label: "Strength", tabs: ["Ashtakavarga", "Shadbala", "Friendship"] },
  { label: "Predictions", tabs: ["Predictions", "Yogas & Doshas", "Numerology"] },
  { label: "Advanced", tabs: ["KP", "Jaimini", "Lal Kitab"] },
  { label: "Timing & Remedies", tabs: ["Transit", "Remedies", "Panchang"] },
];

export default function ResultPage() {
  const [data, setData] = useState<KundliResponse | null>(null);
  const [ready, setReady] = useState(false);
  const [tab, setTab] = useState<string>("Premium Report");

  useEffect(() => {
    setData(loadResult());
    setReady(true);
  }, []);

  if (!ready) return <main className="app" />;

  if (!data) {
    return (
      <main className="app">
        <div className="card empty-state">
          <p>No kundli to display. Please generate one first.</p>
          <Link className="btn-ghost" href="/">
            ← Back to form
          </Link>
        </div>
      </main>
    );
  }

  const subtitle = `${formatDate(data.input.localDate)} · ${data.input.localTime} · ${data.input.place.label}`;

  // A chart generated by an older build may lack the newer server-computed sections.
  const k = data.kundli;
  const stale = !k.kp || !k.jaimini || !k.transit || !k.varshaphal || !k.doshas || !k.avkahada;

  function renderTab() {
    switch (tab) {
      case "Premium Report": return <PremiumReport data={data!} />;
      case "Chart": return <Charts data={data!} />;
      case "Divisional": return <DivisionalCharts data={data!} />;
      case "Planets": return <PlanetTable data={data!} />;
      case "Vimshottari": return <DashaTimeline data={data!} />;
      case "Yogini": return <YoginiDasha data={data!} />;
      case "Ashtakavarga": return <Ashtakavarga data={data!} />;
      case "Yogas & Doshas": return (
        <>
          <Yogas data={data!} />
          <h3 className="yoga-group-h" style={{ marginTop: 24 }}>Doshas</h3>
          <Doshas data={data!} />
        </>
      );
      case "Predictions": return <Predictions data={data!} />;
      case "Remedies": return <Remedies data={data!} />;
      case "Numerology": return <Numerology data={data!} />;
      case "Transit": return <Transit data={data!} />;
      case "Friendship": return <FriendshipTable data={data!} />;
      case "KP": return <KpTable data={data!} />;
      case "Jaimini": return <JaiminiPanel data={data!} />;
      case "Varshaphal": return <Varshaphal data={data!} />;
      case "Shadbala": return <Shadbala data={data!} />;
      case "Lal Kitab": return <LalKitab data={data!} />;
      case "Panchang": return <PanchangPanel data={data!} />;
      default: return null;
    }
  }

  return (
    <main className="app">
      <header className="app-header">
        <div className="app-brand">
          <span className="brand-mark">🪐</span>
          <span className="app-brand-name">Astrochakra</span>
        </div>
        <div className="app-person">
          <h1>{data.name}</h1>
          <span className="app-person-sub">{subtitle}</span>
        </div>
        <div className="app-actions">
          <Link className="btn-ghost" href="/">+ New</Link>
          <DownloadButtons data={data} />
        </div>
      </header>

      {data.warnings?.map((w, i) => (
        <div className="warning" key={i}>⚠ {w}</div>
      ))}

      {stale && (
        <div className="warning stale-banner">
          ⟳ This chart was generated by an earlier version, so some sections (KP, Jaimini,
          Transit, Varshaphal…) may be missing.{" "}
          <Link href="/">Regenerate the chart</Link> to load every section.
        </div>
      )}

      <Overview data={data} />

      <div className="app-body">
        <nav className="app-nav" aria-label="Sections">
          {NAV.map((group) => (
            <div className="nav-group" key={group.label}>
              <div className="nav-group-label">{group.label}</div>
              {group.tabs.map((t) => (
                <button
                  key={t}
                  className={`nav-item${t === tab ? " active" : ""}`}
                  onClick={() => setTab(t)}
                >
                  {t}
                </button>
              ))}
            </div>
          ))}
        </nav>

        <section className="app-content card">
          <h2 className="content-title">{tab}</h2>
          {renderTab()}
        </section>
      </div>
    </main>
  );
}
